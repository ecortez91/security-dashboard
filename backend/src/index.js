// Get available fix scripts
app.get('/api/scripts', (req, res) => {
  const platform = req.query.platform || detectPlatform(req.headers['user-agent']);
  res.json({
    platform,
    scripts: getScriptsForPlatform(platform),
    all: scripts
  });
});

// Get specific script info
app.get('/api/scripts/:scriptId', (req, res) => {
  const script = scripts[req.params.scriptId];
  if (!script) {
    return res.status(404).json({ error: 'Script not found' });
  }
  res.json(script);
});

// Download script file
app.get('/api/scripts/:scriptId/download', (req, res) => {
  const { scriptId } = req.params;
  const platform = req.query.platform || 'linux';
  const script = scripts[scriptId];
  
  if (!script) {
    return res.status(404).json({ error: 'Script not found' });
  }
  
  const platformScript = script.platforms?.[platform];
  if (!platformScript) {
    return res.status(404).json({ error: `Script not available for ${platform}` });
  }
  
  // Generate script content based on script ID
  const content = generateScriptContent(scriptId, platform);
  const filename = platform === 'windows' ? `${scriptId}.ps1` : `${scriptId}.sh`;
  
  res.setHeader('Content-Type', 'text/plain');
  res.setHeader('Content-Disposition', `attachment; filename="${filename}"`);
  res.send(content);
});

